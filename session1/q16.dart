// Write a Dart code snippet that demonstrates the use of an arithmetic operator to
// calculate the remainder of dividing 10 by 3.
void main() {
  int dividend = 10; // المقسوم
  int divisor = 3; // المقسوم عليه

  int remainder = dividend % divisor; // حساب الباقي باستخدام العامل %

  print(remainder);
}