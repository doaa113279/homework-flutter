// Write a Dart program that declares a String variable named greeting with an initial
// value of "Hello". Then, update greeting to "Hello, Dart!" and print it.
void main() {
  String greeting = "Hello"; // تعريف المتغير greeting وتعيين القيمة الأولية "Hello"
  print(greeting); // طباعة القيمة الأولية

  greeting = "Hello, Dart!"; // تحديث القيمة إلى "Hello, Dart!"
  print( greeting); // طباعة القيمة المحدثة
}