// Using Dot Operator with Strings
// Objective: Practice using the dot operator with string methods.
// Instructions:
// - Create a string variable greeting with the value 'Hello, Dart!'.
// - Print the length of greeting.
// - Convert greeting to uppercase and print it.
void main() {
  //Create a string variable greeting with the value 'Hello, Dart!'.
  String greeting = 'Hello,Dart!';
  //Print the length of greeting.
  print("the length of greeting: ${greeting.length}");
  //Convert greeting to uppercase and print it.
  print("Convert greeting to uppercase: ${greeting.toUpperCase()}");
}